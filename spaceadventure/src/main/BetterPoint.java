package main;

public class BetterPoint {
    public int x, y, width, height;

    public BetterPoint(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public int getHeight() {
        return height;
    }
    public int getWidth() {
        return width;
    }
    public int width() {
        return width;
    }
    public int height() {
        return height;
    }
}
